# ---------------------------------------
# Criação do modelo
# ---------------------------------------
model = pulp.LpProblem("City_Optimization", pulp.LpMaximize)

# Variáveis de decisão
# x^C_i, x^P_i, x^F_i
xC = pulp.LpVariable.dicts("xC", range(N), cat=pulp.LpBinary)
xP = pulp.LpVariable.dicts("xP", range(N), cat=pulp.LpBinary)
xF = pulp.LpVariable.dicts("xF", range(N), cat=pulp.LpBinary)

# z^{CP}_{ij}, z^{CF}_{ij}, z^{PF}_{ij}
zCP = pulp.LpVariable.dicts("zCP", (range(N), range(N)), cat=pulp.LpBinary)
zCF = pulp.LpVariable.dicts("zCF", (range(N), range(N)), cat=pulp.LpBinary)
zPF = pulp.LpVariable.dicts("zPF", (range(N), range(N)), cat=pulp.LpBinary)

# ---------------------------------------
# Função Objetivo
# (2*N^2+1)*L(z) + F(x,z)
# L(z) = sum sum zCF_ij * A_ij
# F(x,z) = sum (xP_i - xF_i) + sum sum (zCP_ij - zPF_ij)*A_ij
# ---------------------------------------

big_weight = 2*(N**2) + 1

L = pulp.lpSum([zCF[i][j]*adj_matrix[i][j]
               for i in range(N) for j in range(N)])
F_hap = pulp.lpSum([xP[i] - xF[i] for i in range(N)]) + \
    pulp.lpSum([(zCP[i][j] - zPF[i][j])*adj_matrix[i][j]
               for i in range(N) for j in range(N)])

model += big_weight * L + F_hap, "Objective"

# ---------------------------------------
# Restrições
# ---------------------------------------

# Cada vértice com uma única estrutura
for i in range(N):
    model += xC[i] + xP[i] + xF[i] == 1

# O vértice 1 é sempre uma Casa (índice 0 no Python)
model += xC[0] == 1

# Linearizações para z^{CF}: zCF_ij = 1 se xC_i = xF_j = 1
# xC_i + xF_j - 1 <= zCF_ij
# xC_i >= zCF_ij
# xF_j >= zCF_ij
for i in range(N):
    for j in range(N):
        model += xC[i] + xF[j] - 1 <= zCF[i][j]
        model += xC[i] >= zCF[i][j]
        model += xF[j] >= zCF[i][j]

# Analogamente para z^{CP} (Casa-Parque)
for i in range(N):
    for j in range(N):
        model += xC[i] + xP[j] - 1 <= zCP[i][j]
        model += xC[i] >= zCP[i][j]
        model += xP[j] >= zCP[i][j]

# Para z^{PF} (Parque-Fábrica)
for i in range(N):
    for j in range(N):
        model += xP[i] + xF[j] - 1 <= zPF[i][j]
        model += xP[i] >= zPF[i][j]
        model += xF[j] >= zPF[i][j]

# Felicidade não negativa: F(x,z) >= 0
# Como F(x,z) = sum(xP_i - xF_i) + sum((zCP_ij - zPF_ij)*A_ij),
# iremos criar uma restrição explícita:
model += F_hap >= 0

# ---------------------------------------
# Resolver o modelo
# ---------------------------------------
model.solve(pulp.PULP_CBC_CMD(msg=0))

# Verificar status
status = pulp.LpStatus[model.status]
