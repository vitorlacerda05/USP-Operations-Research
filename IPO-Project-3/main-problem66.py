for i in range(N):
    for j in range(N):
        if adj_matrix[i][j] == 1:
            G.add_edge(i+1, j+1)

pos = {i: (positions[i][0], positions[i][1]) for i in positions}

# Colorir os nós de acordo com a estrutura
color_map = []
for i in G.nodes(data=True):
    if i[1]['structure'] == 'house':
        color_map.append('gray')
    elif i[1]['structure'] == 'park':
        color_map.append('green')
    elif i[1]['structure'] == 'factory':
        color_map.append('blue')
    else:
        color_map.append('red')

plt.figure(figsize=(10, 10))
nx.draw_networkx_nodes(G, pos, node_color=color_map, node_size=300)
